def preprocess_data(bus_data, branch_data, S_base_MVA=1.0, V_base_kV=12.66):
    """预处理数据（计算标幺值、映射负荷等）"""
    buses = [int(r[0]) for r in bus_data]
    Zb = (V_base_kV ** 2) / S_base_MVA  # 基准阻抗

    # 负荷数据（kW→MW，kVAr→MVAr）
    Pd_MW = {int(r[0]): float(r[2]) / 1000.0 for r in bus_data}
    Qd_MVAr = {int(r[0]): float(r[3]) / 1000.0 for r in bus_data}

    # 支路数据（阻抗标幺值）
    lines = []
    for r in branch_data:
        i, j = int(r[0]), int(r[1])
        r_pu = float(r[2]) / Zb
        x_pu = float(r[3]) / Zb
        lines.append((i, j, r_pu, x_pu))

    return {
        'buses': buses,
        'slack_bus': 1,
        'Pd_MW': Pd_MW,
        'Qd_MVAr': Qd_MVAr,
        'lines': lines,
        'Zb': Zb
    }
